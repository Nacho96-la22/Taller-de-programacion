package tema1;

public class Demo02OperadoresUnarios {

    public static void main(String[] args) {
        int i = 3; // i vale 3  
        System.out.println(i);
        i++;       // i vale 4
        System.out.println(i);
        i--;      // i vale 3   		   
        System.out.println(i);
    }
    
}
